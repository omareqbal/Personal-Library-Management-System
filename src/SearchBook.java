    private void booksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_booksMouseClicked
        DefaultTableModel tm=(DefaultTableModel) books.getModel();
        int row=books.getSelectedRow();
        int id=Integer.parseInt(tm.getValueAt(row, 0).toString());
        Book b=Book.getDetails(id);
        String borrower=IssueReturn.getBorrower(id);
        id_tf.setText(Integer.toString(b.id));
        name_tf.setText(b.name);
        author_tf.setText(b.author);
        isbn_tf.setText(b.ISBN);
        publisher_tf.setText(b.publisher);
        year_tf.setText(Integer.toString(b.year));
        avail_tf.setText(Integer.toString(b.availability));
        if(b.availability==0)
            borrower_tf.setText(borrower);
        else
            borrower_tf.setText("");
        if(level==1){
            edit_btn.setVisible(true);
            update_btn.setVisible(true);
            remove_btn.setVisible(true);
            borrower_lb.setVisible(true);
            borrower_tf.setVisible(true);
        }
        else{
            edit_btn.setVisible(false);
            update_btn.setVisible(false);
            remove_btn.setVisible(false);
            borrower_lb.setVisible(false);
            borrower_tf.setVisible(false);
        }
            
        CardLayout card = (CardLayout)(mainPanel.getLayout());
        card.show(mainPanel, "details");
        // TODO add your handling code here:
    }//GEN-LAST:event_booksMouseClicked

