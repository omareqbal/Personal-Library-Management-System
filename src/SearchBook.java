    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        int id=Integer.parseInt(id_tf.getText());
        String name=name_tf.getText();
        String author=author_tf.getText();
        String ISBN=isbn_tf.getText();
        String publisher=publisher_tf.getText();
        int year=Integer.parseInt(year_tf.getText());
        if(Book.update(id, name, author, ISBN, publisher, year)==1){
            JOptionPane.showMessageDialog(this,"Book details updated!");
            name_tf.setEditable(false);
            author_tf.setEditable(false);
            isbn_tf.setEditable(false);
            publisher_tf.setEditable(false);
            year_tf.setEditable(false);
            update_btn.setEnabled(false);
        }
        else
        JOptionPane.showMessageDialog(this,"Error!");
        // TODO add your handling code here:
    }//GEN-LAST:event_update_btnActionPerformed

    private void remove_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_btnActionPerformed
        int c=JOptionPane.showConfirmDialog(this,"Do you want to delete?","Delete Record",JOptionPane.YES_NO_OPTION);
        int id=Integer.parseInt(id_tf.getText());
        if(c==JOptionPane.YES_OPTION){
            if(Book.delete(id)==1){
                JOptionPane.showMessageDialog(this,"Book details removed!");
                back_btn.doClick();
            }
            else
            JOptionPane.showMessageDialog(this,"Error!");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_remove_btnActionPerformed

